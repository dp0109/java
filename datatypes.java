public class datatypes {
    public static void main(String[] args) {
        int a = 5;               
        float b = 5.99f;    
        char c = 'D';         
        boolean d = true;       
        String e = "Hello"; 

        System.out.println("int:"+a);
        System.out.println("float:"+b);
        System.out.println("char:"+c);
        System.out.println("boolean:"+d);
        System.out.println("int:"+e);
        

    }
}
